public class Variable {
    public static void main(String args[]){
        int a = 5;
        int b = 3;
        System.out.println("a = "+a);
        System.out.println("b = "+b);
        a = 10;
        System.out.println("a = "+a);
        String name = "Tony";
        System.out.println("name = "+name);
    }
}
